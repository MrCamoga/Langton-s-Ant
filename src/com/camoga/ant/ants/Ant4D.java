	public void init(long rule, long iterations) {
		super.init(rule, iterations);
		match = 2;
		states[2] = -1;
	}

	public void move(long it) {
		int iteration = 0;
		for(; iteration < it; iteration++) {
			changechunk: {
				if(x > cSIZEm) {
					x = 0;
					xc++;
				} else if(x < 0) {
					x = cSIZEm;
					xc--;
				} else if(y > cSIZEm) {
					y = 0;
					yc++;
				} else if(y < 0) {
					y = cSIZEm;
					yc--;
				} else if(z > cSIZEm) {
					z = 0;
					zc++;
				} else if(z < 0) {
					z = cSIZEm;
					zc--;
				} else if(w > cSIZEm) {
					w = 0;
					wc++;
				} else if(w < 0) {
					w = cSIZEm;
					wc--;
				} else break changechunk;
				chunk = map.getChunk(xc, yc, zc, wc);
			}
			
			int index = (((((w<<cPOW)|z)<<cPOW)|y)<<cPOW)|x;
			state = chunk.cells[index];

			dir = transform[(rule.turn[state]<<8) | dir];
			if(++chunk.cells[index] == rule.getSize()) chunk.cells[index] = 0;
			
			x += directionx[dir];
			y += directiony[dir];
			z += directionz[dir];
			w += directionw[dir];
			
			if(findingPeriod()) {
				if(stateindex < states.length) {
					states[(int) stateindex++] = (byte) dir;
					states[(int) stateindex++] = (byte) state;
				} else {
					stateindex+=2;
				}
				
				if(states[match]!=(byte)dir || states[match+1]!=(byte)state) {
					match = 0;
					period = stateindex;
					xend = getX();
					yend = getY();
					zend = getZ();
					wend = getW();
				} else {
					match+=2;
					if(match == states.length || match > Settings.repeatpercent*period) {
						PERIODFOUND = true;
						saveState = false;
						break;
					}
				}
			}
		}
		iterations += iteration;
	}
	
	public int computeHash() {
		int hash = 1;
		for(int h = -2, wc = this.wc, w = this.w+h; h < 3; h++, w++) {
			if(w < 0) {
				w += cSIZE;
				wc--;
			} else if(w > cSIZEm) {
				w-= cSIZE;
				wc++;
			}
			for(int i = -2, zc = this.zc, z = this.z+i; i < 3; i++, z++) {
				if(z < 0) {
					z += cSIZE;
					zc--;
				} else if(z > cSIZEm) {
					z-= cSIZE;
					zc++;
				}
				for(int j = -2, yc = this.yc, y = this.y+j; j < 3; j++, y++) {
					if(y < 0) {
						y += cSIZE;
						yc--;
					} else if(y > cSIZEm) {
						y-= cSIZE;
						yc++;
					}
					for(int k = -2, xc = this.xc, x = this.x+k; k < 3; k++, x++) {
						if(x < 0) {
							x += cSIZE;
							xc--;
						} else if(x > cSIZEm) {
							x-= cSIZE;
							xc++;
						}
						Chunk c = map.getChunk2(xc, yc, zc, wc);
						hash = 31*hash + (c!=null ? c.cells[(((((w<<cPOW)|z)<<cPOW)|y)<<cPOW)|x]:0);
					}
				}
			}
		}
		return hash;
	}
	
	public long getPeriod() {
		return period/2;
	}
